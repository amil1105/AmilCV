		<p class="BakuText">
			Azerbaycan'ın başkenti ve en büyük şehri olan tarihi ve modern bir cazibe merkezidir. Hazar Denizi'nin kıyısında konumlanmış olan bu büyüleyici şehir, zengin kültürel mirası, etkileyici mimarisi ve muhteşem manzaralarıyla ziyaretçilerini cezbeder. Bakü'nün tarihi, binlerce yıl öncesine uzanır. Şehir, dünyanın en eski yerleşimlerinden biri olarak kabul edilen Içerişehir (İçərişəhər) olarak adlandırılan tarihi merkeziyle ünlüdür. UNESCO Dünya Mirası Listesi'nde yer alan bu bölge, dar labirent sokakları, tarihi yapıları ve büyüleyici meydanlarıyla tarihseverlere görsel bir şölen sunar. Şehrin kalbinde yer alan ve 12. yüzyıla kadar uzanan Şirvanşahlar Sarayı gibi önemli yapılar da ziyaretçileri kendine hayran bırakır. Bakü, aynı zamanda modern bir şehir olup yüksek gökdelenler, alışveriş merkezleri, lüks oteller ve restoranlarla doludur. Şehrin sembolü haline gelen Flame Towers (Alev Kuleleri), göz kamaştırıcı bir görsel şölen sunan, gece ışıklarla aydınlanan üç kuleden oluşur. Bu modern yapılar, Bakü'nün çağdaş ve gelişmiş bir şehir olduğunu simgeler. Bakü'nün en ünlü cazibe merkezlerinden biri de Hazar Denizi kıyısında yer alan sahil şerididir. Sahil, uzun yürüyüşlere, spor etkinliklerine ve dinlenmeye imkan tanır. Burada bulunan Bulvar Parkı, renkli çiçeklerle süslenmiş yürüyüş yolları, bisiklet yolları, kafeler ve restoranlarla dolu bir alandır. Akşamları, sahil boyunca yürürken denizin üzerinden yükselen güzel günbatımının tadını çıkarabilirsiniz.
